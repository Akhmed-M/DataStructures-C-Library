#include "reverse.c"

/*---------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------*/

void Test(int size)
{
    stack s = CreateStack(size);

    for (int i = 0; i < size * 2; i++)
        Push(&s, i);

    DisplayStack(s); // INITIAL STATE
    Reverse(&s);
    Reverse(&s);
    DisplayStack(s); // BACK TO INITIAL STATE
}

void main()
{
    Test(10);
}

